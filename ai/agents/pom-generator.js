const fs = require('fs');
const path = require('path');

async function runPomGenerator() {
    const statePath = path.resolve('artifacts/reports/app-state.json');
    if (!fs.existsSync(statePath)) {
        console.error('State file not found. Run explorer first.');
        process.exit(1);
    }

    const appState = JSON.parse(fs.readFileSync(statePath, 'utf-8'));
    const promptTemplate = fs.readFileSync(path.resolve('ai/prompts/pom-generator.md'), 'utf-8');

    const pomPath = path.resolve('apps/web/pages/AutoGeneratedPage.ts');
    let existingPom = '';
    if (fs.existsSync(pomPath)) {
        existingPom = fs.readFileSync(pomPath, 'utf-8');
        console.log('[POM Generator] Found existing POM, performing an additive update...');
    }

    const fullPrompt = `${promptTemplate}
    
### Existing Page Object (Context)
\`\`\`typescript
${existingPom}
\`\`\`

### New Application State (Target: ${appState.url})
\`\`\`json
${JSON.stringify(trimmedElements, null, 2)}
\`\`\`

If an existing POM was provided, only ADD the missing locators and methods. Do not delete existing ones. 
Make sure to include the // @auto-generated tag at the top.
`;

    console.log('[POM Generator] Architecting Page Object Model...');
    const response = await fetch('http://127.0.0.1:11434/api/generate', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ model: 'qwen2.5-coder:7b', prompt: fullPrompt, stream: false })
    });

    const data = await response.json();
    const outputPath = path.resolve('artifacts/reports/GeneratedPOM.md');
    fs.writeFileSync(outputPath, data.response, 'utf-8');
    console.log(`[POM Generator] POM saved to: ${outputPath}`);
}

runPomGenerator().catch(console.error);
